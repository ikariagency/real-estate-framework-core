// Returns coordinates for a zone, with a small deterministic offset based on slug
export function getPropertyCoordinates(zone: string, slug: string): { lat: number; lng: number } | null {
  const base = zoneCoordinates[zone];
  if (!base) return null;
  
  // Deterministic pseudo-random offset based on slug hash to spread markers
  let hash = 0;
  for (let i = 0; i < slug.length; i++) {
    hash = ((hash << 5) - hash) + slug.charCodeAt(i);
    hash |= 0;
  }
  
  const offsetLat = ((hash % 100) / 100) * 0.008 - 0.004;
  const offsetLng = (((hash >> 8) % 100) / 100) * 0.008 - 0.004;
  
  return {
    lat: base.lat + offsetLat,
    lng: base.lng + offsetLng,
  };
}
